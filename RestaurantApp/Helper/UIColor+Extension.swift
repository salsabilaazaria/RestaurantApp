import UIKit

extension UIColor {
	
	public class var bukaRestoDarkGreen: UIColor {
		return UIColor(red: 72, green: 130, blue: 126, alpha: 1)
	}
	
	public class var bukaRestoLightGray: UIColor {
		return UIColor(red: 135, green: 76, blue: 98, alpha: 1)
	}

}

extension UIColor {
	//FONT COLOR
	public class var bukaRestoSecondaryFontColor: UIColor {
		return UIColor(red: 79, green: 79, blue: 79, alpha: 1)
	}
}
