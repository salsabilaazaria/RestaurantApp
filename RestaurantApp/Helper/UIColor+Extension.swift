import UIKit

extension UIColor {
	
	public class var bukaRestoDarkGreen: UIColor {
		return UIColor(red: 72/255.0, green: 130/255.0, blue: 126/255.0, alpha: 1)
	}
	
	public class var bukaRestoLightGray: UIColor {
		return UIColor(red: 135/255.0, green: 76/255.0, blue: 98/255.0, alpha: 1)
	}

}

extension UIColor {
	//FONT COLOR
	public class var bukaRestoSecondaryFontColor: UIColor {
		return UIColor(red: 79/255.0, green: 79/255.0, blue: 79/255.0, alpha: 1)
	}
}
